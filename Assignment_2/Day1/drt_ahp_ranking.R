plotBar <- function(values, title, ylab, file_name, lower_is_better = FALSE) {
  png(file.path(plot_dir, file_name), width = 1200, height = 800, res = 140)
  colors <- rep("#5B9BD5", length(values))
  best_index <- if (lower_is_better) which.min(values) else which.max(values)
  colors[best_index] <- "#70AD47"
  barplot(
    values,
    names.arg = alternatives,
    col = colors,
    main = title,
    ylab = ylab,
    xlab = "Scenario",
    ylim = c(0, max(values) * 1.15)
  )
  grid(nx = NA, ny = NULL, col = "gray85")
  dev.off()
}

plotPie <- function(values, title, file_name) {
  png(file.path(plot_dir, file_name), width = 1200, height = 800, res = 140)
  piepercent <- round(100 * values / sum(values), 1)
  pie(
    values,
    labels = paste0(piepercent, "%"),
    col = c(
      "lightblue", "mistyrose", "lightcyan", "lavender",
      "cornsilk", "thistle4", "steelblue4", "palegreen"
    )[seq_along(values)],
    radius = 1,
    main = title
  )
  legend(
    "bottomleft",
    names(values),
    cex = 0.8,
    fill = c(
      "lightblue", "mistyrose", "lightcyan", "lavender",
      "cornsilk", "thistle4", "steelblue4", "palegreen"
    )[seq_along(values)]
  )
  dev.off()
}

