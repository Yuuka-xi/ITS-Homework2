  total_travel_time = total_travel_time,
  reliability = reliability,
  idle_vehicle_share = idle_vehicle_share,
  environmental_efficiency = environmental_efficiency
)

# -------------------------------
# 4. Pairwise comparison matrix for the criteria
# -------------------------------
#
# Criteria rationale:
#
# Since our decision-making focuses on DRT service quality, service reliability
# and passenger waiting experience receive the highest priority. A rejected
# request means the DRT system fails to serve a passenger, so reliability is
# assigned the highest importance. Extreme waiting time (P95) is slightly more
# important than average waiting time because it captures the worst passenger
# experience and fairness. Average waiting time remains a central passenger
# experience metric. Total travel time is important, but DRT pooling naturally
# allows some detour, so it is lower than reliability and waiting-time criteria.
# Operational efficiency and environmental efficiency are included as supporting
# criteria, but they are less important than direct service-quality criteria.
#
# Based on this reasoning:
# - reliability is 2 times more important than avg_wait,
#   equally to slightly more important than p95_wait,
#   3 times more important than total_travel_time,
#   7 times more important than idle_vehicle_share,
#   and 9 times more important than environmental_efficiency.
# - p95_wait is 2 times more important than avg_wait because it captures
#   extreme passenger experience; it is 3 times more important than total
#   travel time and 5-7 times more important than the supporting indicators.
# - avg_wait is 2 times more important than total_travel_time,
#   5 times more important than idle_vehicle_share,
#   and 7 times more important than environmental_efficiency.
# - idle_vehicle_share is moderately more important than d_p/d_t in this
#   operational study because too many idle vehicles directly imply an oversized
#   DRT fleet and high operating cost.

criteria <- names(APCL)

CWPC <- matrix(c(
  1,   1/2, 2,   1/2, 5,   7,
  2,   1,   3,   1,   5,   7,
  1/2, 1/3, 1,   1/3, 3,   5,
  2,   1,   3,   1,   7,   9,
  1/5, 1/5, 1/3, 1/7, 1,   2,
  1/7, 1/7, 1/5, 1/9, 1/2, 1
),
