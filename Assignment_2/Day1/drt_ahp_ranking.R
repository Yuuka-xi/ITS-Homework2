# AHP ranking for Task3-Cottbus DRT parameter sets
#
# This script follows the AHP structure used in the LCA tutorial:
# 1. Build one pairwise comparison matrix for alternatives under each criterion.
# 2. Bundle these matrices in APCL.
# 3. Define one pairwise comparison matrix for the criteria, CWPC.
# 4. Compute final AHP scores from principal eigenvectors.
#
# Main references / rationale:
# - Saaty, T. L. (1980). The Analytic Hierarchy Process. McGraw-Hill.
# - ITS DRT lecture: DRT insertion objectives include maximizing request
#   acceptance rate, minimizing passenger ride time, minimizing operation time,
#   and minimizing fleet size.
# - Ricard et al. (2021): reliability is an important aspect of public transport
#   service quality.
# - Gerzinic et al. (2023): waiting-time variability affects travellers'
#   perception and future choices; cancelled service has strong disutility.
# - Zang et al. (2022): travel time reliability is a core concern for travellers,
#   operators, planners, and managers.
#
# Note:
# The exact Saaty scores in CWPC are our group judgement. The references support
# the relative importance logic, not a universal fixed matrix for Cottbus.

