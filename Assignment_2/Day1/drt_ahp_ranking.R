# 3. Pairwise comparison matrix for each criterion
# -------------------------------

avg_wait <- buildSaatyMatrix(metrics$avgWait_min_exact, "cost")
p95_wait <- buildSaatyMatrix(metrics$p95Wait_min_exact, "cost")
total_travel_time <- buildSaatyMatrix(metrics$totalTravelTime_min_exact, "cost")
reliability <- buildReliabilityMatrix(metrics$rejections)
idle_vehicle_share <- buildSaatyMatrix(metrics$minShareIdleVehicles, "cost")
environmental_efficiency <- buildSaatyMatrix(metrics$dp_dt, "benefit")

