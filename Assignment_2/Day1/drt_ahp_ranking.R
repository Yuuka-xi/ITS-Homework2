# -------------------------------
# 1. AHP helper functions
# -------------------------------

getWeights <- function(mat) {
  eigen_result <- eigen(mat)
  max_index <- which.max(Re(eigen_result$values))
  eigenvec <- Re(eigen_result$vectors[, max_index])
  weights <- abs(eigenvec)
  weights <- weights / sum(weights)
  return(weights)
}

AHP <- function(criteria_weights, alternatives_mats) {
  criteria_w <- getWeights(criteria_weights)
  alt_weights <- sapply(alternatives_mats, getWeights)
  result <- alt_weights %*% criteria_w
  return(as.vector(result))
}

consistencyRatio <- function(mat) {
  n <- nrow(mat)
  if (n <= 2) {
    return(0)
  }

  lambda_max <- max(Re(eigen(mat)$values))
  ci <- (lambda_max - n) / (n - 1)

  ri_table <- c(
    "1" = 0.00, "2" = 0.00, "3" = 0.58, "4" = 0.90,
    "5" = 1.12, "6" = 1.24, "7" = 1.32, "8" = 1.41,
    "9" = 1.45, "10" = 1.49
  )

  ri <- ri_table[as.character(n)]
  if (is.na(ri) || ri == 0) {
    return(0)
  }

  return(ci / ri)
}

# -------------------------------
# 2. Convert observed differences to Saaty scale
# -------------------------------
#
# For each criterion, the direction of preference comes from the simulation
# result. For example, under average waiting time, a lower value is better.
# The strength of preference is then mapped to Saaty's 1-9 scale according to
# the relative difference between two alternatives.
#
# Example:
# S5 avg wait = 7.51 min, S1 avg wait = 9.17 min.
# Relative difference = (9.17 - 7.51) / 7.51 = 22.1%.
# This falls in the 20%-30% range, so S5 is assigned a Saaty value of 6 over S1.
# The reciprocal entry is therefore 1/6.

gapToSaaty <- function(relative_gap) {
  if (relative_gap < 0.01) {
    return(1)
  } else if (relative_gap < 0.03) {
    return(2)
  } else if (relative_gap < 0.07) {
    return(3)
  } else if (relative_gap < 0.12) {
    return(4)
  } else if (relative_gap < 0.20) {
    return(5)
  } else if (relative_gap < 0.30) {
    return(6)
  } else if (relative_gap < 0.45) {
    return(7)
  } else if (relative_gap < 0.65) {
    return(8)
  } else {
    return(9)
  }
}

buildSaatyMatrix <- function(values, direction = "cost") {
  n <- length(values)
  mat <- matrix(1, nrow = n, ncol = n)

  for (i in 1:n) {
    for (j in 1:n) {
      if (i == j) {
        mat[i, j] <- 1
      } else {
        if (direction == "cost") {
          better_i <- values[i] < values[j]
          better_j <- values[j] < values[i]
          better_value <- min(values[i], values[j])
          worse_value <- max(values[i], values[j])
        } else if (direction == "benefit") {
          better_i <- values[i] > values[j]
          better_j <- values[j] > values[i]
          better_value <- max(values[i], values[j])
          worse_value <- min(values[i], values[j])
        } else {
          stop("direction must be either 'cost' or 'benefit'")
        }

        if (better_value == 0 && worse_value == 0) {
          scale_value <- 1
        } else if (better_value == 0) {
          scale_value <- 9
        } else {
          relative_gap <- abs(worse_value - better_value) / abs(better_value)
          scale_value <- gapToSaaty(relative_gap)
        }

        if (better_i) {
          mat[i, j] <- scale_value
        } else if (better_j) {
          mat[i, j] <- 1 / scale_value
        } else {
          mat[i, j] <- 1
        }
      }
    }
  }

  rownames(mat) <- alternatives
  colnames(mat) <- alternatives
  return(mat)
}

buildReliabilityMatrix <- function(rejections) {
  n <- length(rejections)
  mat <- matrix(1, nrow = n, ncol = n)

  for (i in 1:n) {
    for (j in 1:n) {
      if (i == j || rejections[i] == rejections[j]) {
        mat[i, j] <- 1
      } else if (rejections[i] == 0 && rejections[j] > 0) {
        # A rejected request means service failure for that passenger.
        # Based on this service-reliability interpretation, a zero-rejection
        # alternative is very strongly preferred over a rejected alternative.
        mat[i, j] <- 7
      } else if (rejections[i] > 0 && rejections[j] == 0) {
        mat[i, j] <- 1 / 7
      } else {
        better_i <- rejections[i] < rejections[j]
        better_value <- min(rejections[i], rejections[j])
        worse_value <- max(rejections[i], rejections[j])
        relative_gap <- (worse_value - better_value) / max(1, better_value)
        scale_value <- gapToSaaty(relative_gap)
        mat[i, j] <- ifelse(better_i, scale_value, 1 / scale_value)
      }
    }
  }

  rownames(mat) <- alternatives
  colnames(mat) <- alternatives
  return(mat)
}

